// --- CAESAR CIPHER LOGIC ---

export const encryptCaesar = (text: string, shift: number): string => {
  let result = "";
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    if (char.match(/[a-z]/i)) {
      const code = text.charCodeAt(i);
      const isUpper = code >= 65 && code <= 90;
      const base = isUpper ? 65 : 97;
      // JS % operator can return negative values, so we fix it
      const shifted = String.fromCharCode(((code - base + shift) % 26 + 26) % 26 + base);
      result += shifted;
    } else {
      result += char;
    }
  }
  return result;
};

export const decryptCaesar = (text: string, shift: number): string => {
  return encryptCaesar(text, -shift);
};

