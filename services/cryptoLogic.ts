// MixColumns matrix
const MIX_COLUMNS_MATRIX = [
  [0x02,0x03,0x01,0x01],
  [0x01,0x02,0x03,0x01],
  [0x01,0x01,0x02,0x03],
  [0x03,0x01,0x01,0x02]
];

// L/E tables
const E = new Array(256).fill(0);
const L = new Array(256).fill(0);

const gf_mul_bit = (a: number, b: number, poly: number = 0x11B): number => {
  let res = 0;
  for (let i = 0; i < 8; i++) {
    if ((b & 1) !== 0) {
      res ^= a;
    }
    const high = a & 0x80;
    a = (a << 1) & 0xFF;
    if (high !== 0) {
      a ^= (poly & 0xFF);
    }
    b >>= 1;
  }
  return res;
};

// Initialize tables (Module scope, runs once)
(() => {
  let x = 1;
  for (let i = 0; i < 255; i++) {
    E[i] = x;
    L[x] = i;
    x = gf_mul_bit(x, 0x03);
  }
  E[255] = 1; // filler
})();

const mul_gf_using_LE = (a: number, b: number): number => {
  if (a === 0 || b === 0) return 0;
  return E[(L[a] + L[b]) % 255];
};

// Hex helpers
export const hexToBlock = (hexStr: string): number[] => {
  const s = hexStr.replace(/\s+/g, "");
  if (s.length !== 32) throw new Error("Plaintext must be exactly 32 hex characters (16 bytes).");
  const bytes = [];
  for (let i = 0; i < 32; i += 2) {
    bytes.push(parseInt(s.substr(i, 2), 16));
  }
  return bytes;
};

export const hexToWord = (wordHex: string): number[] => {
  const s = wordHex.replace(/\s+/g, "");
  if (s.length !== 8) throw new Error("Word must be 8 hex characters.");
  const bytes = [];
  for (let i = 0; i < 8; i += 2) {
    bytes.push(parseInt(s.substr(i, 2), 16));
  }
  return bytes;
};

// AES Transforms
export const subBytesState = (state: number[]): number[] => {
  return state.map(b => S_BOX[b]);
};

export const shiftRowsState = (state: number[]): number[] => {
  const out = new Array(16).fill(0);
  for (let r = 0; r < 4; r++) {
    for (let c = 0; c < 4; c++) {
      // Logic from python script: out[r + 4*c] = state[r + 4*((c + r) % 4)]
      // This implies column-major storage in a 1D array
      out[r + 4 * c] = state[r + 4 * ((c + r) % 4)];
    }
  }
  return out;
};

const mixSingleColumnUsingLE = (col: number[]): number[] => {
  const res = [0, 0, 0, 0];
  for (let row = 0; row < 4; row++) {
    let val = 0;
    for (let i = 0; i < 4; i++) {
      const coef = MIX_COLUMNS_MATRIX[row][i];
      if (coef === 0x01) {
        val ^= col[i];
      } else {
        val ^= mul_gf_using_LE(coef, col[i]);
      }
    }
    res[row] = val;
  }
  return res;
};

export const mixColumnsState = (state: number[]): number[] => {
  const out = new Array(16).fill(0);
  for (let c = 0; c < 4; c++) {
    const col = [
      state[4 * c + 0],
      state[4 * c + 1],
      state[4 * c + 2],
      state[4 * c + 3]
    ];
    const mixed = mixSingleColumnUsingLE(col);
    for (let r = 0; r < 4; r++) {
      out[4 * c + r] = mixed[r];
    }
  }
  return out;
};

// W4 Computation
const rotWord = (word: number[]): number[] => {
  return [...word.slice(1), word[0]];
};

const subWord = (word: number[]): number[] => {
  return word.map(b => S_BOX[b]);
};

export const computeW4 = (w0Hex: string, w3Hex: string, rconHex: string): number[] => {
  const w0 = hexToWord(w0Hex);
  const w3 = hexToWord(w3Hex);
  const rcon = hexToWord(rconHex);
  
  let g = rotWord(w3);
  g = subWord(g);
  g = g.map((b, i) => b ^ rcon[i]);
  
  const w4 = w0.map((b, i) => b ^ g[i]);
  return w4;
};

export const formatByte = (b: number) => b.toString(16).toUpperCase().padStart(2, '0');
